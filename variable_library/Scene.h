namespace variableNS {
	class Scene {
	public:
		/////////////////////////////////
		//シーン突入時に呼ばれる
		/////////////////////////////////
		virtual void OnEnter() = 0;

		/////////////////////////////////
		//シーン再生中、毎フレーム呼ばれる
		/////////////////////////////////
		virtual void OnUpdate() = 0;

		/////////////////////////////////
		//シーン終了時に呼ばれる
		/////////////////////////////////
		virtual void OnExit() = 0;
	};
}
#endif // !SCENE